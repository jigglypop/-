a = [(1, 2), (0, 1), (5, 1), (5, 2), (3, 0)]
b = sorted(a)
# [(0, 1), (1, 2), (3, 0), (5, 1), (5, 2)]
c = sorted(a, key=lambda x: x[0])
# [(0, 1), (1, 2), (3, 0), (5, 1), (5, 2)]

d = sorted(a, key=lambda x: x[1])
# [(3, 0), (0, 1), (5, 1), (1, 2), (5, 2)]

# 첫번째 오름차순, 두번째 내림차순
e = [(1, 3), (0, 3), (1, 4), (1, 5), (0, 1), (2, 4)]
f = sorted(e, key=lambda x: (x[0], -x[1]))
# [(0, 3), (0, 1), (1, 5), (1, 4), (1, 3), (2, 4)]
